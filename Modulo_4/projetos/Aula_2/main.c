int main(){
	int idade;
	char opc;
	
	printf("Qual a sua idade?:\n");
	scanf("%d", &idade);
	
	printf("Voce tem carro? Digite (s) para SIM e (n) para NAO:\n");
	scanf(" %c", &opc);
	
	if(idade <= 18 && idade >= 50)
		printf("O seu auxilio vale R$200\n");
	
	if(opc == 'n' || opc == 'N'){
		if(idade >= 18 && idade <= 50)
			printf("O seu auxilio vale R$500\n");
	}
	
	if(opc == 's' || opc == 'S'){
		if(idade <= 18 && idade >= 50)
			printf("O seu auxilio vale R$200\n");
		else
			printf("O seu auxilio vale R$200\n");
	}
	
	
	return 0;
} /*end main*/









