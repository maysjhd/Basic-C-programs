int main(){
	
	float num1=0,
		  num2=0,
		  adi=0,
		  mult=0,
		  sub=0;
		  
	printf("\nEntre com o primeiro valor:");
	scanf("%f", &num1);
	printf("\nEntre com o segundo valor:");
	scanf("%f", &num2);
	
	adi = num1  + num2;
	mult = num1 * num2;
	sub = num1  - num2;
	
	printf("\nA soma entre %.2f e %.2f resultou em: %.2f\n", num1, num2, adi);
	printf("\nO produto entre %.2f e %.2f resultou em: %.2f\n", num1, num2, mult);
	printf("\nA subtracao entre %.2f e %.2f resultou em: %.2f\n", num1, num2, sub);
	
	return 0;
} /*end main*/
