int main(){
	int n, tab;
	tab = 1;
	while(tab <= 9){
		n = 1;
		printf("Tabuada do %d\n",tab);
		while(n <= 10){
			printf("%2d x %2d = %2d \n", tab, n, tab * n);	
			n++;
		}	
	tab++;
	putchar('\n\n');
	}
	return 0;
} /*end main*/



