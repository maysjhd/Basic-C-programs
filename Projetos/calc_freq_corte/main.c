/* ---- Função Principal ----*/

int main(){
	
	float c = 1.0E-6,
		  r = 3.0E3,
		  pi = 3.14,
		  f;
		  
	f = 1/(2*pi*r*c);
	
	printf("A frequencia de corte eh: %.2f", f);
	
	return 0;
} /*end main*/
